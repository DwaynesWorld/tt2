use tokio::signal;

extern crate log;

pub mod logger;

pub fn create_shutdown_signals() -> (impl Future<Output = ()>, impl Future<Output = ()>) {
    let ctrl_c = async {
        signal::ctrl_c()
            .await
            .expect("failed to install Ctrl+C handler");
    };

    #[cfg(unix)]
    let terminate = async {
        signal::unix::signal(signal::unix::SignalKind::terminate())
            .expect("failed to install signal handler")
            .recv()
            .await;
    };

    #[cfg(not(unix))]
    let terminate = std::future::pending::<()>();

    (ctrl_c, terminate)
}
